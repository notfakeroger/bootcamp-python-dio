# Divisão (Retorna números flutuantes (float))

valor_1 = int(input("Digite o primeiro número: "))
valor_2 = int(input("Digite o segundo número: "))

print(f"A divisão entre os valores inseridos é de {valor_1 / valor_2} ")

# Divisão inteira (Retorna apenas nuúmeros inteiros)

