# Atribuição Simples

saldo = 500

print(saldo)

# Com adição

saldo = 500
saldo += 200

print(saldo)

# Com subtração

saldo = 500
saldo -= 100

print(saldo)

# Com multiplicação

saldo = 500
saldo *= 2

print(saldo)

# Com divisão e divisão inteira

saldo = 500
saldo /= 5

print(saldo)

saldo = 500
saldo //= 5

print(saldo)

# Com módulo

saldo = 500
saldo %= 480

print(saldo)

# Com exponenciação

saldo = 80
saldo **= 2

print(saldo)